
package aprenderjavaconejercicios;

public class OperacionesVariables {
    public static void main(String [] args){
    
        int x;
        int y;
        x=144;
        y=999;
        int suma=x+y;
        int resta=x-y;
        int multiplicacion=x*y;
        double division=(double)x/y;
        String nombre="George Boole";
        
        System.out.println("El valor de x es: "+x);
        System.out.println("El valor de y es: "+y);
        System.out.println("La suma de x más y es: "+suma);
        System.out.println("La resta de x menos y es: "+resta);
        System.out.println("El producto de x por y es: "+multiplicacion);
        System.out.println("La división de x entre y es: "+division);
        System.out.println("Mi nombre es: "+nombre);
    }
    
}
